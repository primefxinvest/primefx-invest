for (const [locale, patch] of Object.entries(localePatches)) {
  const target = translations[locale]
  for (const [keyPath, value] of Object.entries(patch)) {
    setNested(target, keyPath, value)
  }
  for (const [keyPath, value] of Object.entries(enFlat)) {
    if (!keyPath.split('.').reduce((obj, part, index, parts) => {
      if (index === parts.length - 1) return obj && Object.prototype.hasOwnProperty.call(obj, part)
      return obj?.[part]
    }, target)) {
      setNested(target, keyPath, value)
    }
  }
  fs.writeFileSync(path.join(messagesDir, `${locale}.json`), `${JSON.stringify(target, null, 2)}\n`)
}

